const root = document.body;
const translationNodes = document.querySelectorAll("[data-i18n]");
const languageButtons = document.querySelectorAll("[data-lang]");
const navToggle = document.querySelector("[data-nav-toggle]");
const nav = document.querySelector("[data-nav]");
const publicationsToggle = document.querySelector("[data-publications-toggle]");
const publicationsPanel = document.querySelector("#all-publications");

function setLanguage(language) {
  const table = translations[language];
  root.dataset.language = language;
  document.documentElement.lang = language;
  translationNodes.forEach((node) => {
    const key = node.dataset.i18n;
    if (table[key]) node.textContent = table[key];
  });
  languageButtons.forEach((button) => button.classList.toggle("active", button.dataset.lang === language));
  if (publicationsToggle) {
    const key = publicationsPanel.hidden ? "publications.toggleOpen" : "publications.toggleClose";
    publicationsToggle.textContent = table[key];
  }

  localStorage.setItem("ece-site-language", language);
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

if (navToggle && nav) {
  navToggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("open");
    navToggle.setAttribute("aria-expanded", String(isOpen));
  });
  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      nav.classList.remove("open");
      navToggle.setAttribute("aria-expanded", "false");
    });
  });
}

if (publicationsToggle && publicationsPanel) {
  publicationsToggle.addEventListener("click", () => {
    publicationsPanel.hidden = !publicationsPanel.hidden;
    const language = root.dataset.language || "en";
    publicationsToggle.textContent = translations[language][publicationsPanel.hidden ? "publications.toggleOpen" : "publications.toggleClose"];
  });
}

const observer = new IntersectionObserver((entries) => {
  entries.forEach((entry) => {
    if (entry.isIntersecting) {
      entry.target.classList.add("visible");
      observer.unobserve(entry.target);
    }
  });
}, { threshold: 0.18 });

document.querySelectorAll(".reveal").forEach((element) => observer.observe(element));
setLanguage(localStorage.getItem("ece-site-language") || "en");
